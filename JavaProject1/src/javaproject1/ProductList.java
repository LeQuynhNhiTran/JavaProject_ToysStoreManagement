    private void Add_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_btActionPerformed
        if(tf_PdID.getText().equals("")||tf_PdName.getText().equals("")||tf_PdCate.getText().equals("")||tf_Price.getText().equals("")||tf_Stock.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Not enough information.");
        }
        else
        {
            if(manage.ProductIDValid(this.tf_PdID.getText(), listProduct)==false){
                JOptionPane.showMessageDialog(this,"Product Already Exist.");
            }else{
                Product product = new Product();
                product.setpdID(this.tf_PdID.getText());
                product.setPdName(this.tf_PdName.getText());
                product.setCategory(this.tf_PdCate.getText());   
                product.setPrice(Double.parseDouble(this.tf_Price.getText()));     
                product.setPrice(Integer.parseInt(this.tf_Stock.getText()));
                listProduct.add(product);
                
                manage.writeProductToFile(listProduct);       
            
                DefaultTableModel tblModel = new DefaultTableModel(header, 0);    
                Vector data = null;
                for(Product pd:listProduct)
                {
                    data = new Vector();
                    data.add(pd.getPdID());
                    data.add(pd.getPdName());
                    data.add(pd.getCategory());
                    data.add(pd.getPrice());
                    data.add(pd.getStock());
                
                    tblModel.addRow(data);
                }
                this.Product_tb.setModel(tblModel);
                JOptionPane.showMessageDialog(this,"Adding successful.");
            }
            
        }
    }//GEN-LAST:event_Add_btActionPerformed

