    private void Edit_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_btActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tbt = (DefaultTableModel) Product_tb.getModel();
        if(Product_tb.getSelectedRowCount()==1)
        {
            String pdID = tf_PdID.getText();
            String pdName = tf_PdName.getText();
            String pdCate = tf_PdCate.getText();
            double pdPrice = Double.parseDouble(tf_Price.getText());
            int pdStock = Integer.parseInt(tf_Stock.getText());
            listProduct.set(Product_tb.getSelectedRow(), new Product(pdID,pdName,pdCate,pdPrice,pdStock));
            manage.writeProductToFile(listProduct);
            
            tbt.setValueAt(pdID, Product_tb.getSelectedRow(), 0);
            tbt.setValueAt(pdName, Product_tb.getSelectedRow(), 1);        
            tbt.setValueAt(pdCate, Product_tb.getSelectedRow(), 2);
            tbt.setValueAt(pdPrice, Product_tb.getSelectedRow(), 3);
            tbt.setValueAt(pdStock, Product_tb.getSelectedRow(), 4);
        }
        else{
            JOptionPane.showMessageDialog(this,"Chose A Row To Edit.");
        }
    }//GEN-LAST:event_Edit_btActionPerformed

    private void Delete_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_btActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tbtModel = (DefaultTableModel) Product_tb.getModel();
        
        if(Product_tb.getSelectedRowCount()==1)
        {
            int i=Product_tb.getSelectedRow();
            tbtModel.removeRow(Product_tb.getSelectedRow());
            listProduct.remove(i); 
            manage.writeProductToFile(listProduct);
        }
        else {
            if(Product_tb.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(this,"No Data.");
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Chose A Row To Delete.");
            }
        }
    }//GEN-LAST:event_Delete_btActionPerformed

