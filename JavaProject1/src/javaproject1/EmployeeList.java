    private void Delete_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_btActionPerformed
        DefaultTableModel tbtModel = (DefaultTableModel) Employee_tb.getModel();
        
        if(Employee_tb.getSelectedRowCount()==1)
        {
            int i=Employee_tb.getSelectedRow();
            tbtModel.removeRow(Employee_tb.getSelectedRow());
            listEmployee.remove(i); 
            manage.writeEmployeeToFile(listEmployee);
        }
        else {
            if(Employee_tb.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(this,"No Data.");
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Chose A Row To Delete.");
            }
        }
    }//GEN-LAST:event_Delete_btActionPerformed

    private void Edit_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_btActionPerformed
        DefaultTableModel tbt = (DefaultTableModel) Employee_tb.getModel();
        if(Employee_tb.getSelectedRowCount()==1)
        {
            String empID = tf_empID.getText();
            String empName = tf_empName.getText();
            String empPhone = tf_empPhone.getText();
            String position = tf_Position.getText();
            listEmployee.set(Employee_tb.getSelectedRow(), new Employee(empID,empName,empPhone,position));
            manage.writeEmployeeToFile(listEmployee);
            
            tbt.setValueAt(empID, Employee_tb.getSelectedRow(), 0);
            tbt.setValueAt(empName, Employee_tb.getSelectedRow(), 1);        
            tbt.setValueAt(empPhone, Employee_tb.getSelectedRow(), 2);
            tbt.setValueAt(position, Employee_tb.getSelectedRow(), 3);
        }
        else{
            JOptionPane.showMessageDialog(this,"Chose A Row To Edit.");
        }
    }//GEN-LAST:event_Edit_btActionPerformed

