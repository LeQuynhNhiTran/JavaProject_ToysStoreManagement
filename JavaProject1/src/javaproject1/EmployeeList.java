    private void Search_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_btActionPerformed
        String srch = tf_Search.getText();
    
        if (srch != null) {
            filter(srch);
        } else {
            JOptionPane.showMessageDialog(this,"Employee is not exist.");
        }
    }//GEN-LAST:event_Search_btActionPerformed
    
    private void filter(String query){
    DefaultTableModel tbtModel = (DefaultTableModel) Employee_tb.getModel();
    TableRowSorter<DefaultTableModel> tr= new TableRowSorter<DefaultTableModel>(tbtModel);
    Employee_tb.setRowSorter(tr);

    tr.setRowFilter(RowFilter.regexFilter(query));
    }
