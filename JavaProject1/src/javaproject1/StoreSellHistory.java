    private void filter(String query){
    DefaultTableModel tbtModel = (DefaultTableModel) Sell_tb.getModel();
    TableRowSorter<DefaultTableModel> tr= new TableRowSorter<DefaultTableModel>(tbtModel);
    Sell_tb.setRowSorter(tr);

    tr.setRowFilter(RowFilter.regexFilter(query));
    }
    
