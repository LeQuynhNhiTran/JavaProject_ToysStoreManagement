    private void Edit_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_btActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tbt = (DefaultTableModel) Sell_tb.getModel();
        if(Sell_tb.getSelectedRowCount()==1)
        {
            String shID = tf_ShID.getText();
            String pdName = tf_PdName.getText();
            String pdCate = tf_Cate.getText();
            String empName = tf_EmpName.getText();
            int pdUnit = Integer.parseInt(tf_Unit.getText());
            double pdPrice = Double.parseDouble(tf_Price.getText());
            
            listSell.set(Sell_tb.getSelectedRow(), new SellHistory(shID,pdName,pdCate,empName,pdUnit,pdPrice));
            manage.writeSellToFile(listSell);
            
            tbt.setValueAt(shID, Sell_tb.getSelectedRow(), 0);
            tbt.setValueAt(pdName, Sell_tb.getSelectedRow(), 1);        
            tbt.setValueAt(pdCate, Sell_tb.getSelectedRow(), 2);
            tbt.setValueAt(empName, Sell_tb.getSelectedRow(), 3);
            tbt.setValueAt(pdUnit, Sell_tb.getSelectedRow(), 4);
            tbt.setValueAt(pdPrice, Sell_tb.getSelectedRow(), 5);
        }
        else{
            JOptionPane.showMessageDialog(this,"Chose A Row To Edit.");
        }
    }//GEN-LAST:event_Edit_btActionPerformed

    private void Delete_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_btActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tbtModel = (DefaultTableModel) Sell_tb.getModel();
        
        if(Sell_tb.getSelectedRowCount()==1)
        {
            int i=Sell_tb.getSelectedRow();
            tbtModel.removeRow(Sell_tb.getSelectedRow());
            listSell.remove(i); 
            manage.writeSellToFile(listSell);
        }
        else {
            if(Sell_tb.getRowCount()==0)
            {
                JOptionPane.showMessageDialog(this,"No Data.");
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Chose A Row To Delete.");
            }
        }
    }//GEN-LAST:event_Delete_btActionPerformed

